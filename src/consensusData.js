// Map: name → consensus rank (1-indexed)
export function getConsensusRank(name) {
  const idx = CONSENSUS_BOARD.indexOf(name);
  return idx >= 0 ? idx + 1 : 999;
}

// Grade estimate from consensus position
// Top pick ~ 96, pick 32 ~ 82, pick 100 ~ 63, pick 200 ~ 48, pick 300+ ~ 38
export function getConsensusGrade(name) {
  const rank = getConsensusRank(name);
  if (rank <= 5) return Math.round(97 - rank * 0.6);
  if (rank <= 15) return Math.round(94 - (rank - 5) * 0.6);
  if (rank <= 32) return Math.round(88 - (rank - 15) * 0.35);
  if (rank <= 64) return Math.round(82 - (rank - 32) * 0.35);
  if (rank <= 100) return Math.round(71 - (rank - 64) * 0.22);
  if (rank <= 150) return Math.round(63 - (rank - 100) * 0.16);
  if (rank <= 224) return Math.round(55 - (rank - 150) * 0.12);
  if (rank <= 319) return Math.round(46 - (rank - 224) * 0.06);
  return 35;
}

