// Map: name → consensus rank (1-indexed)
export function getConsensusRank(name) {
  const idx = CONSENSUS_BOARD.indexOf(name);
  return idx >= 0 ? idx + 1 : 999;
}

// Round projection from consensus rank
export function getConsensusRound(name) {
  const rank = getConsensusRank(name);
  if (rank <= 11) return { label: "RD 1", bg: "#171717", fg: "#fff" };
  if (rank <= 39) return { label: "RD 1-2", bg: "#525252", fg: "#fff" };
  if (rank <= 60) return { label: "RD 2", bg: "#737373", fg: "#fff" };
  if (rank <= 97) return { label: "RD 3", bg: "#a3a3a3", fg: "#fff" };
  if (rank <= 134) return { label: "RD 4", bg: "#c8c8c8", fg: "#737373" };
  if (rank <= 171) return { label: "RD 5", bg: "#d4d4d4", fg: "#737373" };
  if (rank <= 208) return { label: "RD 6", bg: "#e0e0e0", fg: "#a3a3a3" };
  if (rank <= 245) return { label: "RD 7", bg: "#e8e8e8", fg: "#a3a3a3" };
  return { label: "UDFA", bg: "#f0f0f0", fg: "#b8b8b8" };
}

// Grade estimate from consensus position
// Top pick ~ 96, pick 32 ~ 82, pick 100 ~ 63, pick 200 ~ 48, pick 450 ~ 30
export function getConsensusGrade(name) {
  const rank = getConsensusRank(name);
  if (rank <= 5) return Math.round(97 - rank * 0.6);
  if (rank <= 15) return Math.round(94 - (rank - 5) * 0.6);
  if (rank <= 32) return Math.round(88 - (rank - 15) * 0.35);
  if (rank <= 64) return Math.round(82 - (rank - 32) * 0.35);
  if (rank <= 100) return Math.round(71 - (rank - 64) * 0.22);
  if (rank <= 150) return Math.round(63 - (rank - 100) * 0.16);
  if (rank <= 224) return Math.round(55 - (rank - 150) * 0.12);
  if (rank <= 320) return Math.round(46 - (rank - 224) * 0.06);
  if (rank <= 450) return Math.round(40 - (rank - 320) * 0.04);
  return 30;
}

